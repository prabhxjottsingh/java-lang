public class dataType {
    public static void main(String args[]){
        
        float marks = 5.7f; // by default values will be treated as double 
        int num1 = 10;
        byte by = 109;
        short sh = 558;
        long lg = 5857l; //by drfault values will be treated as integer
        char ch = 'k';
        boolean flag = false;

        System.out.println("These are the marks in float value: " + marks);
        System.out.println("These are the values in int: " + num1);
        System.out.println("These are the values in byte: " + by);
        System.out.println("These are the values in short: " + sh);
        System.out.println("These are the values in char: " + ch);
        System.out.println("These are the values in long: " + lg);
        System.out.println("These are the values in bool: " + flag);

    }
}
